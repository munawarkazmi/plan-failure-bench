"""Generate the paper's results tables from the committed run records.

The working paper's prose is written by hand; its numbers are not. This
tool derives every table in the results section directly from
results/*.jsonl using the same rescoring and metrics code as the rest of
the repository, so the paper cannot drift from the data. Rerun it after
any completed run and commit the regenerated .tex files.

Outputs:
  paper/generated/run_summary.tex        one row per complete committed run
  paper/generated/hallucination_table.tex  hallucinated_entity counts per
                                            obfuscated run, the token
                                            confusability artefact record
"""

import os
import sys

sys.path.insert(0, os.getcwd())

from plan_failure_bench.instructions import load_seeds
from plan_failure_bench.loader import load_environment
from plan_failure_bench.metrics import detection_report, observed_verdict
from plan_failure_bench.rescore import rescore_records
from plan_failure_bench.runner import load_records

SUITES = {
    "house_01": load_seeds("instructions/seeds_house_01.json"),
    "office_01": load_seeds("instructions/seeds_office_01.json"),
}
ENVS = {
    "house_01": {"house_01": load_environment("environments/house_01.json")},
    "office_01": {"office_01": load_environment("environments/office_01.json")},
}

# Every complete committed run, in presentation order. The superseded
# qwen v1-token run stays in the table because the paper discusses the
# artefact it produced; it is marked as superseded rather than hidden.
RUNS = [
    ("Llama 3.3 70B", "house\\_01", "plain", "results/groq_llama70b_plain.jsonl", "house_01", ""),
    ("Llama 3.3 70B", "house\\_01", "obfuscated (v1)", "results/groq_llama70b_obfuscated.jsonl", "house_01", ""),
    ("Qwen 2.5 7B", "house\\_01", "plain", "results/local_qwen_plain.jsonl", "house_01", ""),
    ("Qwen 2.5 7B", "house\\_01", "obfuscated (v1)", "results/local_qwen_obfuscated.jsonl", "house_01", "superseded"),
    ("Qwen 2.5 7B", "house\\_01", "obfuscated (v2)", "results/local_qwen_obfuscated_v2.jsonl", "house_01", ""),
    ("Gemini 3.1 Flash Lite", "house\\_01", "plain", "results/gemini_flash_lite_plain.jsonl", "house_01", ""),
    ("Gemini 3.1 Flash Lite", "house\\_01", "obfuscated (v2)", "results/gemini_flash_lite_obfuscated.jsonl", "house_01", ""),
    ("Qwen 2.5 7B", "office\\_01", "plain", "results/local_qwen_office_plain.jsonl", "office_01", ""),
    ("Qwen 2.5 7B", "office\\_01", "obfuscated (v2)", "results/local_qwen_office_obfuscated.jsonl", "office_01", ""),
    ("Gemini 3.1 Flash Lite", "office\\_01", "plain", "results/gemini_flash_lite_office_plain.jsonl", "office_01", ""),
    ("Gemini 3.1 Flash Lite", "office\\_01", "obfuscated (v2)", "results/gemini_flash_lite_office_obfuscated.jsonl", "office_01", ""),
]


def run_stats(path, suite_key):
    raw = load_records(path)
    if len(raw) != 30:
        raise SystemExit(f"{path}: expected 30 records, found {len(raw)}; partial runs do not enter the paper")
    strict_malformed = sum(1 for r in raw if r["verdict"] == "malformed")
    lenient = rescore_records(raw, SUITES[suite_key], ENVS[suite_key], policy="lenient")
    report = detection_report(lenient)
    detected = sum(s.detected for s in report.per_label.values())
    trap_total = sum(s.total for s in report.per_label.values())
    exact = sum(s.reason_correct for s in report.per_label.values())
    valid_solved = sum(1 for r in lenient if r["label"] == "valid" and observed_verdict(r) == "valid")
    valid_total = sum(1 for r in lenient if r["label"] == "valid")
    hallucinated = sum(1 for r in lenient if observed_verdict(r) == "hallucinated_entity")
    return {
        "strict_malformed": strict_malformed,
        "detected": detected,
        "trap_total": trap_total,
        "exact": exact,
        "fp": report.false_positives,
        "feasible": report.feasible_total,
        "valid_solved": valid_solved,
        "valid_total": valid_total,
        "hallucinated": hallucinated,
    }


stats = {path: run_stats(path, suite) for _, _, _, path, suite, _ in RUNS}

summary = [
    "% Generated by tools/build_paper_results.py from results/*.jsonl.",
    "% Do not edit by hand; rerun the tool after any completed run.",
    "\\begin{tabular}{llllrrrrr}",
    "\\toprule",
    "Model & Environment & Condition & Note & \\shortstack[l]{Format\\\\failures} & \\shortstack[l]{Traps\\\\detected} & \\shortstack[l]{Exact\\\\reasons} & \\shortstack[l]{False\\\\positives} & \\shortstack[l]{Valid\\\\solved} \\\\",
    "\\midrule",
]
for model, env_tex, condition, path, _, note in RUNS:
    s = stats[path]
    summary.append(
        f"{model} & {env_tex} & {condition} & {note} & "
        f"{s['strict_malformed']}/30 & {s['detected']}/{s['trap_total']} & {s['exact']} & "
        f"{s['fp']}/{s['feasible']} & {s['valid_solved']}/{s['valid_total']} \\\\"
    )
summary += ["\\bottomrule", "\\end{tabular}"]

hallucination = [
    "% Generated by tools/build_paper_results.py from results/*.jsonl.",
    "% Do not edit by hand; rerun the tool after any completed run.",
    "\\begin{tabular}{lllr}",
    "\\toprule",
    "Model & Environment & Token scheme & Hallucinated entities \\\\",
    "\\midrule",
]
for model, env_tex, condition, path, _, note in RUNS:
    if "obfuscated" not in condition:
        continue
    scheme = "v1" if "v1" in condition else "v2"
    hallucination.append(f"{model} & {env_tex} & {scheme} & {stats[path]['hallucinated']} \\\\")
hallucination += ["\\bottomrule", "\\end{tabular}"]

os.makedirs("paper/generated", exist_ok=True)
for name, lines in (("run_summary.tex", summary), ("hallucination_table.tex", hallucination)):
    with open(f"paper/generated/{name}", "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote paper/generated/" + name)
